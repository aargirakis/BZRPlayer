	move.w	dtg_SndNum(A5),D1
	moveq	#0,D0
	move.b	SongNumber(PC,D1.W),D0
	clr.l	-(SP)
	move.l	D0,-(SP)
	bsr.w	InitMusic
	addq.l	#8,SP
	rts

